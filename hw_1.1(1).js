/* Переменная хранит в себе значение от 0 до 9. 
Написать скрипт который будет выводить слово “один”, если переменная хранит значение 1. 
Выводить слово “два” - если переменная хранит значение 2, и т.д. для всех цифр от 0 до 9. 
Реализовать двумя способами. 2-ой способ. */
let num = prompt("Введите число: ");
if(num == 1) {
    alert("Один");
}
if(num == 2) {
    alert("Два");
}
if(num == 3) {
    alert("Три");
}
if(num == 4) {
    alert("Четыре");
}
if(num == 5) {
    alert("Пять");
}
if(num == 6) {
    alert("Шесть");
}
if(num == 7) {
    alert("Семь");
}
if(num == 8) {
    alert("Восемь");
}
if(num == 9) {
    alert("Девять");
}
if(num == 0) {
    alert("Ноль");
} else if (num > 9) {
    alert("Введите число, которое входит в промежуток 0-9");
}